import Foundation

//Used to remove duplicate when filtering

extension Array where Element: Equatable {
    
    func removeDuplicates() -> [Element] {
        var result = [Element]()
        for value in self {
            if !result.contains(value) {
                result.append(value)
            }
        }
        return result
    }
}
